"""SX Bet venue adapters — FakeVenueAdapter and SXBetAdapter skeleton.

:class:`FakeVenueAdapter` is fully deterministic and offline-safe; it drives
all offline tests without any network calls.

:class:`SXBetAdapter` is a config-gated skeleton.  The SX Bet API is a
decentralised peer-to-peer exchange (SX Network, chainId 4162) whose order
submission requires EIP-712 wallet signatures generated by ``eth-account``.
Full live wiring is deferred until ``eth-account`` is confirmed as a project
dependency; live methods raise :exc:`NotImplementedError` behind the
``sx_bet_enabled`` config flag.

Research note (2026-06-28)
--------------------------
Authoritative SX Bet REST endpoints confirmed via Context7 (source:
``https://docs.sx.bet``):

- ``POST /orders`` — place a maker/taker order; requires EIP-712 signature.
- ``GET  /orders?maker=<address>`` — list open orders for a maker address.
- ``POST /orders/cancel/v2`` — cancel specific orders by hash (EIP-712 signed).
- ``POST /orders/cancel/all`` — cancel all open orders (EIP-712 signed).
- ``GET  /markets`` — fetch available markets.

Base URLs:
- Mainnet: ``https://api.sx.bet``
- Testnet: ``https://api.toronto.sx.bet`` (safe default; see ``sx_bet_base_url``)

Authentication uses EIP-712 wallet signatures (not a bearer token), so
credentials are ``sx_bet_maker_address`` and ``sx_bet_private_key``.

IMPORTANT: ``httpx`` is imported **lazily** inside async methods so that
``import veridex.venues.sx_bet`` is offline-safe and pulls no network library
at module load time (CON-010 async-shell / sync-core split).
"""

