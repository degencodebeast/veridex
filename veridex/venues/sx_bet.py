class SXBetAdapter:
    """Live SX Bet venue adapter — config-gated skeleton.

    The SX Bet API is a decentralised exchange (SX Network) using EIP-712
    wallet signatures for all order operations.  Full live wiring requires
    ``eth-account`` (not yet in project deps) and an active wallet.

    Live methods raise :exc:`NotImplementedError` until both:

    1. ``sx_bet_enabled = true`` is set in ``veridex/.env``.
    2. ``SX_BET_MAKER_ADDRESS`` and ``SX_BET_PRIVATE_KEY`` are configured.
    3. ``eth-account`` is added as a project dependency for EIP-712 signing.

    Constructor is dependency-free — :func:`~veridex.config.get_settings` is
    accessed only when a live method is called, keeping module import
    offline-safe (CON-010).

    ``httpx`` is imported **lazily** inside async methods — never at module
    level.
    """

    def __init__(self) -> None:
        """Initialise the adapter.

        No settings access here — credentials are resolved lazily inside
        the live methods to keep ``import veridex.venues.sx_bet`` offline-safe.
        """

    def _get_live_settings(self) -> tuple[Settings, str, str, str]:
        """Resolve and validate live settings; raise if the gate is not open.

        Returns:
            A ``(settings, base_url, maker_address, private_key)`` tuple.

        Raises:
            NotImplementedError: If ``sx_bet_enabled`` is ``False``.
            ValueError: If required credentials are missing.
        """
        from veridex.config import get_settings, require_sx_bet  # lazy config import

        settings = get_settings()
        if not settings.sx_bet_enabled:
            raise NotImplementedError("SX Bet live path pending: set SX_BET_ENABLED=true and provide credentials")
        maker_address, private_key = require_sx_bet(settings)
        return settings, settings.sx_bet_base_url, maker_address, private_key

